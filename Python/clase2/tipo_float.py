# profundizando en el tipo float

a = 3.0
print(f'a: {a:.2f}')

# constructor de tipo float -> puede recbir enteros y strings
b = float(10) # tipo entero
print(f'b: {b}')

c = float('10') # tipo string
print(f'c: {c}')

# notaci√≥n exponencial
d = 3e5
print(f'd: {d:.2f}')

e = 3e-5
print(f'e: {e:.5f}')

f = 4.0 + 5
print(f)